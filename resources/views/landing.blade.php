Terlibat banyak event dan pernikahan bersama Ega Robot Ethnic Percussion, Parade lagu daerah “Gita Permata Nusantara” Taman Mini Indonesia Indah. Juga pernah berprestasi Juara Harapan dan favorit disetiap pasanggiri mendapat Juara 1 dan umum diacara pasanggiri dan evaluasi. Awal terbentuknya Sanggar bulan Mei 2020 diikuti 15 murid yang berusia antara 5 sampai 12 tahun terbuka untuk TK – SD – SMP – SMA sederajat.

